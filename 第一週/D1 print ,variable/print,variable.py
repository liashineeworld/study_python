#print 列印
print('hello,world')

#跨多行列印 ''' '''
print('''0000
      
      0000''')

#斷行 \n
print('hello\nworld')

#不斷行 end=''
print('hello',end='')
print('world')

#type判斷資料類型
print(type('hello,world'))
print(type(123))
print(type(3.14159))

#int取整數 小數點無條件捨去
print(3.14159,int(3.14159))

#round 四捨五入
print(3.123,round(3.123))
print(3.841,round(3.841))
print(6.157,round(6.157,2))  #小數2位

